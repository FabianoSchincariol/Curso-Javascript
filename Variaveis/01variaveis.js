//Variáveis

/**
 * https://developer.mozilla.org/pt-BR/docs/Web/JavaScript/Reference/Statements/var
 * Declarações de variáveis, onde quer que elas ocorram, 
 * são processadas antes que qualquer outro código seja 
 * executado. O escopo de uma variável declarada com  
 * var é seu contexto atual em execução, o qual é a função
 *  a qual pertence ou, para variáveis declaradas fora de
 *  qualquer função, o escopo é o global.
 */


/**
 * https://developer.mozilla.org/pt-BR/docs/Web/JavaScript/Reference/Statements/let
 * let permite que você declare variáveis limitando 
 * seu escopo no bloco, instrução, ou em uma expressão 
 * na qual ela é usada. Isso é inverso da keyword var,
 *  que define uma variável globalmente ou no escopo 
 * inteiro de uma função, independentemente do escopo 
 * de bloco.
 */

// Declarando uma variável
let a; //declaração somente a variável sem atribuição
let b = 0; //declarando uma variável e atribuindo o valor 0
//atribuindo um valor a variável a crianda acima
a = 10;
//declarando uma variável com número inteiro
let c = 1;
//declarando uma variável atribuindo número real
let d = 0.001;
//declarando uma string de texto
let strAspasDuplas = "Olá!!!";
let strAspasSimples = 'Bem vindo!';
//declarando uma variável e atribuindo um valor booleano
let verdadeOUFalso = true;//verdadeiro
let ligadoVentilador = false;//falso

/**
 * https://developer.mozilla.org/pt-BR/docs/Web/JavaScript/Reference/Global_Objects/null
 * 
 * O valor null é um literal em JavaScript que representa 
 * um valor nulo ou "vazio" (p/ex: que aponta para um 
 * objeto inexistente, veremos em breve sobre objeto).
 * É um dos valores primitivos do JavaScript.
 */

 //declarando variável com um valor nulo
let e = null;//nulo

/**
 * https://developer.mozilla.org/pt-BR/docs/Web/JavaScript/Reference/Global_Objects/undefined
 * O valor global undefined representa um valor indefinido.
 * Trata-se de um dos tipos primitivos do JavaScript.
 */

 //declarando uma variável undefined
 let f = undefined;//indefinido

